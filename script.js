$(window).scroll(function() {    
    var scroll = $(window).scrollTop();
    if (scroll >= 215) {
        $("nav").addClass("sticky-box");
        $("#upper").fadeIn();
    }
    if (scroll<215) {
      $("nav").removeClass("sticky-box");
      $("#upper").fadeOut();
    }


});

